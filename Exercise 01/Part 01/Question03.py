#Simple Calculator
Num1 = int(input("Enter First Number: "))
Num2 = int(input("Enter Second Number: "))
Operator =input("Enter Operator: ")
if(Operator == "+"):
    print("Answer is: ",Num1+Num2)
elif(Operator == "-"):
    print("Answer is: ",Num1-Num2)
elif(Operator == "*"):
    print("Answer is: ",Num1*Num2)
elif(Operator == "/"):
    print("Answer is: ",Num1/Num2)


